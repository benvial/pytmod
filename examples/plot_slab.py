# Summary
# -------
# This example demonstrated:
#
# 1. **Dispersion relations** in time-modulated materials showing how the
#    band structure changes with modulation amplitude.
#
# 2. **Fundamental harmonic scattering** showing reflection and transmission
#    spectra for different slab thicknesses and modulation strengths.
#
# 3. **Sideband generation** - the time modulation creates scattered fields
#    at frequencies shifted by multiples of the modulation frequency.
#
# 4. **Thickness dependence** - comparing thin and thick slabs shows how
#    interference effects and resonances depend on the optical path length.
#
# These results are consistent with the Floquet scattering theory presented
# in Zurita-Sáncez et al. (2009), demonstrating how temporal modulation can
# be used to control light propagation and generate new frequency components.
